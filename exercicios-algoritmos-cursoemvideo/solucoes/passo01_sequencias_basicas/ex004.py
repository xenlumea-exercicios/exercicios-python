# 4) Desenvolva um algoritmo que leia dois números inteiros e mostre o somatório entre eles.
# Ex:
# Digite um valor: 8
# Digite outro valor: 5
# A soma entre 8 e 5 é igual a 13.

primeiro_numero = int(input('Digite o primeiro número inteiro: '))
segundo_numero = int(input('Digite o segundo número inteiro: '))

soma = primeiro_numero + segundo_numero

print(f'A soma entre {primeiro_numero} e {segundo_numero} é igual a {soma}.')
